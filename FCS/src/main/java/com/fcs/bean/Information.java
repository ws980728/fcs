package com.fcs.bean;

public class Information {

	private String informationId;
	private String informationTopic;
	private String informationAuthor;
	private String informationContent;
	private String informationFabulous;
	private String informationReltime;
	private String informationStatus;
	private String informationImg;

	public String getInformationId() {
		return informationId;
	}

	public void setInformationId(String informationId) {
		this.informationId = informationId;
	}

	public String getInformationTopic() {
		return informationTopic;
	}

	public void setInformationTopic(String informationTopic) {
		this.informationTopic = informationTopic;
	}

	public String getInformationAuthor() {
		return informationAuthor;
	}

	public void setInformationAuthor(String informationAuthor) {
		this.informationAuthor = informationAuthor;
	}

	public String getInformationContent() {
		return informationContent;
	}

	public void setInformationContent(String informationContent) {
		this.informationContent = informationContent;
	}

	public String getInformationFabulous() {
		return informationFabulous;
	}

	public void setInformationFabulous(String informationFabulous) {
		this.informationFabulous = informationFabulous;
	}

	public String getInformationReltime() {
		return informationReltime;
	}

	public void setInformationReltime(String informationReltime) {
		this.informationReltime = informationReltime;
	}

	public String getInformationStatus() {
		return informationStatus;
	}

	public void setInformationStatus(String informationStatus) {
		this.informationStatus = informationStatus;
	}

	public String getInformationImg() {
		return informationImg;
	}

	public void setInformationImg(String informationImg) {
		this.informationImg = informationImg;
	}

}
